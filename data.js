// 添加方法
function addClass(str, ele) {
	ele.className += (' ' + str);
}

// 删除
function removeClass(str, ele) {
	var classes = ele.className.split(' ');
	var newClass = [];
	classes.forEach(function(e) {
		if (e !== str) {
			newClass.push(e);
		}
	});
	ele.className = newClass.join(' ');
}

